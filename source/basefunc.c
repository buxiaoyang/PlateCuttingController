#include <basefunc.h>

/***************************************************************************/
// 延迟1us函数
// 参数：延迟时间（us）
// 返回值：无	
/***************************************************************************/
void delay_us(uint us_value)
{
	uint k;
	while(us_value--)
	{
		k++;
	}
}

/***************************************************************************/
// 延迟1ms函数
// 参数：延迟时间（ms）
// 返回值：无	
/***************************************************************************/
void delay_ms(uint ms_value)
{
	uint k;
	for(k=0;k<ms_value;k++)
	{
		delay_us(478);
	}
}

